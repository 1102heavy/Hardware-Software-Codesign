# ============================================================
# Allocate buffers
# One shared input buffer is okay because DMAs run sequentially.
# Separate output buffer per DMA is cleaner for checking results.
# ============================================================

in_buf = allocate(shape=(NUMBER_OF_INPUT_WORDS,), dtype=np.int32)
out_bufs = [
    allocate(shape=(NUMBER_OF_OUTPUT_WORDS,), dtype=np.int32)
    for _ in DMA_NAMES
]

# ============================================================
# Helper functions
# ============================================================

def fill_input_for_case(buf):
    for i in range(NUMBER_OF_INPUT_WORDS):
        buf[i] = test_input_memory[i]

def expected_output_for_case(mem):
    A = np.array(mem[:512], dtype=np.int32).reshape(64, 8)
    B = np.array(mem[512:520], dtype=np.int32)
    exp = np.sum(A * B, axis=1) >> 8
    return exp.astype(np.int32)

def timer_reset_and_start(timer_ip):
    timer_ip.write(TCSR0, 0x00000000)
    timer_ip.write(TLR0, 0x00000000)
    timer_ip.write(TCSR0, XTC_CSR_LOAD_MASK)
    timer_ip.write(TCSR0, 0x00000000)
    timer_ip.write(TCSR0, XTC_CSR_ENABLE_TMR_MASK)

def timer_stop_and_read(timer_ip):
    cycles = timer_ip.read(TCR0)
    timer_ip.write(TCSR0, 0x00000000)
    return cycles

def run_dma_once(dma_ip, in_buffer, out_buffer, timer_ip):
    out_buffer[:] = 0

    timer_reset_and_start(timer_ip)

    # RX first, then TX
    dma_ip.recvchannel.transfer(out_buffer)
    dma_ip.sendchannel.transfer(in_buffer)

    dma_ip.sendchannel.wait()
    dma_ip.recvchannel.wait()

    cycles = timer_stop_and_read(timer_ip)
    return cycles

# ============================================================
# Run tests
# ============================================================

all_ok = True
exp = expected_output_for_case(test_input_memory)

for test_case_cnt in range(NUMBER_OF_TEST_VECTORS):
    print(f"\n===== Test case {test_case_cnt} =====")

    fill_input_for_case(in_buf)

    dma_results = []

    for i, dma_ip in enumerate(dmas):
        dma_name = DMA_NAMES[i]
        print(f"\nRunning {dma_name} ...")

        cycles = run_dma_once(dma_ip, in_buf, out_bufs[i], timer)
        ok = np.array_equal(out_bufs[i], exp)
        all_ok = all_ok and ok

        dma_results.append((dma_name, cycles, ok))

        print("Received output:")
        print(out_bufs[i])
        print("\nExpected output:")
        print(exp)
        print(f"\nMatch for {dma_name}: {ok}")
        print(f"Measured AXI Timer cycles for {dma_name}: {cycles}")

        time_us = cycles / TIMER_CLOCK_HZ * 1e6
        print(f"Approx elapsed time for {dma_name}: {time_us:.3f} us")

# ============================================================
# Final result
# ============================================================

print("\n===== Summary =====")
for dma_name, cycles, ok in dma_results:
    time_us = cycles / TIMER_CLOCK_HZ * 1e6
    print(f"{dma_name}: cycles = {cycles}, time = {time_us:.3f} us, match = {ok}")

print("\n===== Final result =====")
